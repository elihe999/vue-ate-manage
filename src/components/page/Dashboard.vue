<script>
    import Schart from 'vue-schart';
    import bus from '../common/bus';
    export default {
        name: 'dashboard',
        data() {
            return {
                name: localStorage.getItem('ms_username'),
                todoList: [{
                        title: '今天要修复100个bug',
                        status: false,
                    }
                ],
                data: [],
                data1: [],
                options: {
                    title: 'Failed case over past seven days',
                    showValue: false,
                    fillColor: 'red',
                    bottomPadding: 30,
                    topPadding: 30
                },
                options2: {
                    title: 'Past seven days suite',
                    fillColor: '#FC6FA1',
                    axisColor: '#008ACD',
                    contentColor: '#EEEEEE',
                    bgColor: '#F5F8FD',
                    bottomPadding: 30,
                    topPadding: 30
                },
                url: ''
            }
        },
        components: {
            Schart
        },
        computed: {
            role() {
                return this.name === 'admin' ? '超级管理员' : '普通用户';
            }
        },
        created(){
            this.handleListener();
            this.getData();
        },
        activated(){
            this.handleListener();
        },
        deactivated(){
            window.removeEventListener('resize', this.renderChart);
            bus.$off('collapse', this.handleBus);
        },
        methods: {
            handleListener(){
                bus.$on('collapse', this.handleBus);
                // 调用renderChart方法对图表进行重新渲染
                window.addEventListener('resize', this.renderChart)
            },
            handleBus(msg){
                setTimeout(() => {
                    this.renderChart()
                }, 300);
            },
            renderChart(){
                this.$refs.bar.renderChart();
                this.$refs.line.renderChart();
            },
            getData() {
                this.url = "/index.php/Welcome/simplechart"
                this.$axios.post(this.url).then((res) => {
                    let temp = []
                    let temp_suite = []
                    res.data.list.forEach(function(currentValue) {
                        let timeObj = new Date(parseInt(currentValue['time']) * 1000)
                        let year = timeObj.getFullYear()
                        let mouth = timeObj.getMonth() + 1
                        let day = timeObj.getDate()
                        let temp_time = year + '/' + mouth + '/' + day
                        temp.push({'value':currentValue['failnum'],'name':temp_time})
                        temp_suite.push({'value':currentValue['totalnum'],'name':temp_time})
                    })
                    this.data = temp
                    this.data1 = temp_suite
                })
            }
        }
    }

</script>


